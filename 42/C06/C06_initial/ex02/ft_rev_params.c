#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_rev_params(char *str)
{
	unsigned int	i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int	main(int argc, char **argv)
{
	int	i;

	(void)argc;
	(void)argv;
	i = argc;
	while (i > 1)
	{
		ft_rev_params(argv[i - 1]);
		ft_putchar('\n');
		i--;
	}
}
